const regionNames = new Intl.DisplayNames(['en'], { type: 'region' });

const ISO_TO_NAME: Record<string, string> = {};
for (const code of ISO_CODES) {
  const name = regionNames.of(code);
  if (name) ISO_TO_NAME[code] = name;
}

const NAME_TO_ISO: Array<[string, string]> = (() => {
  const entries: Array<[string, string]> = [];
  for (const code of ISO_CODES) {
    const name = ISO_TO_NAME[code]?.toLowerCase();
    if (name) entries.push([name, code]);
  }
  for (const [alias, code] of Object.entries(ALIASES)) {
    entries.push([alias.toLowerCase(), code]);
  }
  entries.sort((a, b) => b[0].length - a[0].length);
  return entries;
})();

function getCountryName(code: string): string {
  return ISO_TO_NAME[code.toUpperCase()] ?? code.toUpperCase();
}

function resolveCountryCode(input: string): string | undefined {
  const normalized = input.trim().toLowerCase();
  if (!normalized) return undefined;
  if (normalized.length === 2 && ISO_TO_NAME[normalized.toUpperCase()]) {
    return normalized.toUpperCase();
  }
  const exact = NAME_TO_ISO.find(([name]) => name === normalized);
  return exact?.[1];
}

